package homework;

public class For_Loop_Odd_Even {
    public static void main(String[] args) {

        for(int i= 1; i<=10; i++)
        {
            System.out.println("Odd numbers :" + i);
            i++;
        }
        for(int i= 0; i<=10; i++)
        {
            System.out.println("Even numbers :" + i);
            i++;
        }
    }
}
