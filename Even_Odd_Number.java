package homework;

public class Even_Odd_Number {
    public static void main(String[] args) {
        int num = 10;

        //To find given number is even or odd.

        if(num % 2 == 0){
            System.out.println(num + " " +"is even...");
        }else
        {
            System.out.println(num + " " + "is odd...");
        }
    }
}
